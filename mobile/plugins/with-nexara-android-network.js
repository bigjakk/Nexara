/**
 * Expo config plugin — writes an Android `network_security_config.xml`
 * and wires it into AndroidManifest.xml.
 *
 * Two changes for the same use case (self-hosted Nexara reachability):
 *
 *   1. cleartextTrafficPermitted="true" — pairs with the M6 item 1
 *      HTTP soft warning. Without this, Android 9+ (targetSdkVersion 28+)
 *      blocks plain HTTP traffic outright regardless of what the app
 *      requests, so the "Continue over HTTP" button on the server-URL
 *      prompt would silently fail. The user has already explicitly
 *      acknowledged the risk via the in-app dialog before this kicks in.
 *
 *   2. <certificates src="user" /> — trust user-installed CAs in addition
 *      to system CAs. This is the v1.1 self-signed cert support: a
 *      Nexara admin running their own private CA can export their root
 *      cert, install it via Android Settings → Security → Encryption
 *      & credentials → Install a certificate → CA certificate, and then
 *      Nexara will accept the resulting connections. Without this,
 *      Android 7+ ignores user-installed CAs for app traffic by default.
 *
 * This is a custom plugin (rather than `expo-build-properties`) because
 * `expo-build-properties` exposes `usesCleartextTraffic` but not the
 * full network_security_config knob — and we specifically need the
 * `<certificates src="user" />` element which is only configurable via
 * the XML file.
 *
 * Trade-off note: trusting user CAs means any cert the user installs
 * on their device gets trusted by Nexara — including corporate MITM
 * proxies if the user is on a managed device. For Nexara's audience
 * (Proxmox admins running self-hosted gear) this is the right call.
 * For users in corporate environments, the corporate proxy can already
 * inspect everything else on the device anyway.
 */

const { withAndroidManifest, withDangerousMod } = require("@expo/config-plugins");
const fs = require("fs");
const path = require("path");

const XML_FILENAME = "network_security_config.xml";

const XML_CONTENT = `<?xml version="1.0" encoding="utf-8"?>
<!--
  Generated by mobile/plugins/with-nexara-android-network.js during
  \`npx expo prebuild\`. Do not edit by hand — changes will be
  overwritten on the next prebuild. Edit the plugin instead.
-->
<network-security-config>
  <base-config cleartextTrafficPermitted="true">
    <trust-anchors>
      <certificates src="system" />
      <certificates src="user" />
    </trust-anchors>
  </base-config>
</network-security-config>
`;

function withNexaraAndroidNetwork(config) {
  // Step 1: write the XML file into android/app/src/main/res/xml/.
  // withDangerousMod runs after the platform project is materialised
  // but before the manifest mod runs, so the file is in place when
  // the manifest references it.
  config = withDangerousMod(config, [
    "android",
    async (cfg) => {
      const xmlDir = path.join(
        cfg.modRequest.platformProjectRoot,
        "app",
        "src",
        "main",
        "res",
        "xml",
      );
      fs.mkdirSync(xmlDir, { recursive: true });
      fs.writeFileSync(path.join(xmlDir, XML_FILENAME), XML_CONTENT);
      return cfg;
    },
  ]);

  // Step 2: point AndroidManifest.xml's <application> at the XML file
  // and set usesCleartextTraffic on the same element. Both attributes
  // are namespace-prefixed; the @expo/config-plugins manifest mod
  // gives us a parsed object whose attributes live under `$`.
  config = withAndroidManifest(config, (cfg) => {
    const app = cfg.modResults.manifest.application?.[0];
    if (!app) return cfg;
    if (!app.$) {
      // Type-narrowing for older config-plugins versions; in practice
      // the application element always has an attribute bag.
      app.$ = {};
    }
    app.$["android:networkSecurityConfig"] = "@xml/network_security_config";
    app.$["android:usesCleartextTraffic"] = "true";
    return cfg;
  });

  return config;
}

module.exports = withNexaraAndroidNetwork;
